export const getHomepageTranslation = (language, section, key, index = null) => {
  const translations = homepageTranslations[language] || homepageTranslations.en;
  if (section && key) {
    if (index !== null && Array.isArray(translations[section])) {
      return translations[section][index]?.[key] || homepageTranslations.en[section][index]?.[key] || '';
    }
    
    // Handle nested keys like 'trustedPartners.title'
    if (key.includes('.')) {
      const keys = key.split('.');
      let value = translations[section];
      let fallbackValue = homepageTranslations.en[section];
      
      for (const k of keys) {
        value = value?.[k];
        fallbackValue = fallbackValue?.[k];
      }
      
      return value || fallbackValue || '';
    }
    
    return translations[section]?.[key] || homepageTranslations.en[section]?.[key] || '';
  }
  return translations || homepageTranslations.en;
}; 